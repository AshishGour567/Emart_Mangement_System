    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row=tblDelete.getSelectedRow();
        if(row==-1)
        {
            JOptionPane.showMessageDialog(null,"No row selected","please select a row",JOptionPane.INFORMATION_MESSAGE);
                return;
        }
        String pid = tm.getValueAt(row,0).toString();
        try{
            if(ProductsDAO.deleteProduct(pid))
            {
                JOptionPane.showMessageDialog(null,"Product Deleted successfully","Success",JOptionPane.INFORMATION_MESSAGE);
                tm.setRowCount(0);
                loadStockDetails();
                return;
            }
            JOptionPane.showMessageDialog(null,"Product not  Deleted ");
        }
        catch(SQLException ex)
        {
           JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
      
        
    }//GEN-LAST:event_btnDeleteActionPerformed

