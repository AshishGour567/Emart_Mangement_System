    private void loadStockDetails() {
        try{
            List<ProductsPojo> al = ProductsDAO.getProductDetails();
            if(al.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"No product present in stock","Empty stock",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            btnDelete.setEnabled(true);
            Object [] rows = new Object[7];
            tm = (DefaultTableModel)tblDelete.getModel();
            for(ProductsPojo p:al)
            {
                rows[0] = p.getProductId();
                rows[1] =  p.getProductName();
                rows[2] = p.getProductCompany();
                rows[3] = p.getProductPrice();
                rows[4] = p.getOurPrice();
                rows[5] = p.getQuantity();
                rows[6] = p.getTax()+"%";
                tm.addRow(rows);
            }
         }
        catch(SQLException ex)
       {
           JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
       }
    }
